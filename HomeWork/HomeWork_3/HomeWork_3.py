import random

some_list = [random.randint(1, 1000)for i in range(int(input("Введите длину списка: ")))]
print(f'Начальный список: {some_list}')
X = int(input('Введите искомое число X: '))
some_set = set(some_list)
temp1 = 0
temp2 = 0

for i in range(1, max(some_set)):
	if X - i in some_set:
		temp1 = X - i
		break
for i in range(1, max(some_set)):
	if X + i in some_set:
		temp2 = X + i
		break
if X - temp1 < temp2 - X:
	print(temp1)
else:
	print(temp2)