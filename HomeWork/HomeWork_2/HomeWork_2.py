# Task 1

from random import randint


n = int(input('Введите количество монет: '))
side = []
side0 = 0
side1 = 0

for i in range(n):
	side.append(randint(0, 1))
	if side[i] == 1:
		side1 += 1
	else:
		side0 += 1

print(f'Монеты на столе: {side}')

if side0 < side1:
	print(f'Необходимо перевернуть {side0} монет')
else:
	print(f'Необходимо перевернуть {side1} монет')